    /**
     * @Route("/{program}/season/{season}/episode/{id}", name="episode_show", methods={"GET"})
     */
    public function showEpisode(Episode $episode): Response
    {
        return $this->render('episode/show.html.twig', [
            'episode' => $episode,
        ]);
    }

    /**
     * @Route("/{program}/season/{season}/episode/{id}/edit", name="episode_edit", methods={"GET","POST"})
